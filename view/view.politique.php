    <h2>Politique de confidentialité</h2>
</div>


<div>
Nous comprenons l'importance de la protection de vos informations personnelles. <br>
C'est pourquoi nous avons élaboré cette politique de confidentialité pour vous informer sur les données que nous collectons, comment nous les utilisons et comment nous les protégeons.
<br><br>
<li class="point-title">Informations collectées</li>
Nous collectons les noms d'utilisateurs, les photos de profil, les adresses e-mail et les mots de passe cryptés de nos utilisateurs. Nous stockons également les livres présents dans leur bibliothèque personnelle et les informations sur leur suivi. Il n'y a pas de cookies sur le site.
<br><br>
<li class="point-title">Utilisation des informations</li>
Les informations collectées sont utilisées pour vous fournir un accès à votre compte et à votre bibliothèque personnelle, ainsi que pour vous permettre de suivre les livres que vous avez enregistrés. Elles ne sont partagées avec aucun tiers.
<br><br>
<li class="point-title">Stockage des informations</li>
Vos informations sont stockées sur nos serveurs sécurisés. <br> Le compte est automatiquement fermé après 3 ans de non-connexion, ainsi que toutes les données qui y sont associées.
<br><br>
<li class="point-title">Modification et suppression des informations</li>
Vous avez le droit de modifier toutes vos informations à partir de votre profil, en respectant les normes d'encodage (nom d'utilisateur avec au moins 3 caractères, adresse e-mail valide, etc.). Vous avez également le droit de supprimer votre compte à partir de votre profil, ainsi que toutes les informations associées.
<br><br>
<li class="point-title">Nom d'utilisateur et photo de profil inappropriés</li>
L'utilisateur est tenu de choisir un nom d'utilisateur et une image de profil qui sont appropriés et respectueux envers les autres utilisateurs. En cas de nom d'utilisateur offensant ou inapproprié ainsi que d'une image de profil inappropriée, l'administrateur se réserve le droit de suspendre temporairement le compte jusqu'à ce que l'utilisateur modifie ces informations.
<br><br>
<li class="point-title">Modifications de la politique de confidentialité</li>
Nous nous réservons le droit de modifier cette politique de confidentialité à tout moment. Les modifications seront publiées sur cette page. Nous vous encourageons à consulter régulièrement cette page pour vous tenir informé des changements.

<br><br><br>
Si vous avez des questions concernant notre politique de confidentialité, veuillez nous contacter à l'adresse e-mail contact@fmusaeva.com
<br><br><br>
Merci d'avoir choisi notre site. <br>
Nous nous efforçons de protéger vos informations personnelles et de vous fournir un service de qualité.

